int	ft_sqrt(int nb)
{
	int	i;
	int	num;

	i = 0;
	num = 0;
	if (nb == 0)
	{
		return (0);
	}
	while (i <= nb)
	{
		num = i * i;
		if (num == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
